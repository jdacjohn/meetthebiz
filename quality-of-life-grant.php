<h2>	ACTORS FOR AUTISM RECEIVES A $4,000 QUALITY OF LIFE GRANT FROM THE 
	CHRISTOPHER &amp; DANA REEVE FOUNDATION FOR "MEET THE BIZ WORKSHOPS"</h2>


<p>	<strong>SHERMAN OAKS, CA - MARCH 18, 2013 - ACTORS FOR AUTISM</strong> is proud to announce that it has received a <strong>$4,000</strong> Quality of Life grant from the Christopher &amp; Dana Reeve Foundation.  The award was one of 76 grants totaling almost $427,000 awarded by the Reeve Foundation to nonprofit organizations nationwide that provide more opportunities, access, and daily quality of life for individuals living with paralysis, their families, and caregivers. Created by the late Dana Reeve, the program has awarded 2,131 grants totaling over $16 million since 1999.  </p>

<p>	<strong>ACTORS FOR AUTISM</strong> will use the grant for the <strong>MEET THE BIZ WORKSHOPS</strong>. The workshops are for the Entertainment Industry to connect with all types of actors and professionals in the Entertainment Industry for a 40 night workshop, whether living with paralysis, blindness, deafness, Down syndrome, Autism, or able-bodied, the workshops involve everyone to further their knowledge and connections. </p>

<p>	<strong>ACTORS FOR AUTISM IS HONORED TO RECEIVE THIS GENEROUS GRANT FROM THE CHRISTOPHER &amp; DANA REEVE FOUNDATION TO SUPPORT THE MEET THE BIZ PROGRAM</strong>.  Meet the Biz Workshops are looking to make acceptance of inclusion and diversity a common place.  The Christopher &amp; Dana Reeve Foundation's grant will allow us to gather a melting pot of professional actors and other professionals in the Entertainment Industry, who can then have a springboard for future work that can have a positive and healthy change in the arts.</p>

	<p>"The Reeve Foundation's Quality of Life Grants Program has always been a cornerstone of one of our core beliefs, the power of we. By supporting these organizations that align with the same beliefs as ours, we are better able to help our community members live more independently,"said Peter T. Wilderotter, president and CEO of the Christopher &amp; Dana Reeve Foundation. "We are pleased to reward a number of tremendous organizations for their efforts."</p>

	<p>The Christopher &amp; Dana Reeve Foundation Quality of Life grants program was conceived by the late Dana Reeve to address the myriad of needs of children and adults with paralysis and other mobility impairments and their families. Grants support critical life-enhancing and life-changing initiatives that improve physical and emotional health and increase independence. Funded projects offer a diversity of services and approaches: improving access; providing education and job training; sponsoring organized sporting activities; and much more. Quality of Life grants are funded through a cooperative agreement with the Centers for Disease Control and Prevention (Award #1U59DD000838).  </p>



	<p><strong>About the Reeve Foundation</strong><br/>
	The Christopher &amp; Dana Reeve Foundation is dedicated to curing spinal cord injury by funding innovative research and improving the quality of life for people living with paralysis through grants, information and advocacy. We meet all 20 of the Better Business Bureau's standards for charity accountability and hold the BBB's Charity Seal. The Paralysis Resource Center (PRC) is a program of the Reeve Foundation, and is funded through a cooperative agreement with the Centers for Disease Control and Prevention (CDC) Award No. 1U59DD000838. For more information, please visit our website at www.ChristopherReeve.org or call 800-539-7309.</p>

