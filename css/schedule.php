<strong><a name="bruce">BRUCE VILANCH</a></strong>&nbsp; <a href="#top" style="font-size: 11px; color:#bd8a5c;">back to top</a><br>
One of the most sought-after joke smiths in the entertainment industry, Bruce Vilanch has become a recognizable face in his own right, thanks to the feature-length documentary "Get Bruce!" (1999) and his one-time stint as a regular on "Hollywood Squares", for which he also served as head writer.

<p>Known for his eclectic eyewear and collection of unique T-shirts, he began as a child model for Lane Bryant, but when his initial efforts as an actor came to naught, he became an entertainment writer for the Chicago Tribune instead. Impressed by his review of her cabaret performance in 1970, Bette Midler hired him to punch up her act where he gave birth to Midler's famed Sophie Tucker jokes. He later moved to Los Angeles where he wrote for variety shows like the original "Donny and Marie" and "The Brady Bunch Hour" (both ABC) and provided material for Richard Pryor, Lily Tomlin and Joan Rivers. He has maintained his connection with Midler through the years, working on such projects as the feature "Divine Madness" (1980) and the TV special "Bette Midler--Diva Las Vegas" (HBO, 1997), as well as writing for her short-lived but lively sitcom, "Bette!" (CBS, 2000 ).
</p>
<p>Vilanch began contributing to Academy Awards telecasts in 1989, collaborating with the likes of Billy Crystal, Whoopi Goldberg and David Letterman, and graduated to head writer in 2000. He has roasted celebrities like Elizabeth Taylor and President Clinton. The Whoopi connection led to his providing the notorious material Ted Danson performed in blackface at a certain 1993 Friars Club roast of his then-girlfriend Goldberg, but his resume also includes "You Made Me Watch You", the touching Emmy-winning valedictory Midler crooned as a send-off to Johnny Carson.
</p>
<p>He scripted most of the Academy Awards telecasts over the last decade, two of which won Emmys, including the one featuring Billy Crystal's Hannibal Lecter entrance. Mr. Vilanch has also written for The Tony's, The Grammy's, The Emmy's, and just about every other award show to brighten your television screen. He also participates and volunteers at almost every charity function, especially those dealing with GLBT issues.
</p>
<p>Bruce was a major hit in the touring company of the  Broadway hit, "Hairspray" playing Edna Turnblad.  
</p>
<p>His new film "Oy Vey! My Son is Gay!!" was just released!</p>


