<strong><a name="lindsay">LINDSAY WAGNER</a></strong>&nbsp; <a href="#best" style="font-size: 11px; color:#bd8a5c;">back to top</a><br>
Lindsay Wagner makes little distinction between her life as an actress, advocate, mother, humanitarian or author.   What unites these various parts is a commitment through her work and her personal life to advancing human potential.  Early in her career this commitment was evident in her Emmy Award winning portrayal of "The Bionic Woman".  Her use of media as a way to communicate ideas to help people in their personal process is demonstrated in so many of her films. 

<p>Films such as:  "Shattered Dreams" on spousal abuse and domestic violence starred in and co-Produced by Lindsay in 1991; "The Taking of Flight 847" on the root complexities of terrorism (1988); "Evil In Clear River" on the quiet rise of the Neo-Nazi movement in America (1988); "Child's Cry" on child sexual abuse (1985);  "I Want To Live" on the moral dilemma regarding capital punishment (1983); and "The Incredible Journey of Doctor Meg Laurel" on the battle between the naturopathic and allopathic healthcare (1979).
</p>
<p>Off screen, Lindsay continuously works both publicly and privately in advocacy and public education.  She shares the knowledge and experiences, which have greatly impacted her life and have profoundly enhanced her awe of our often unrecognized human potential.  Lindsay has co-authored two books: a best-selling vegetarian lifestyle cookbook entitled, The High Road to Health (Simon & Schuster) and a book on acupressure, Lindsay Wagner's New Beauty: The Acupressure Facelift (Simon & Schuster).  
</p>
<p>From 2003-2006 Lindsay co-facilitated a support group for convicted batterers and their families.  In 2004 she co-founded "Peacemakers Community", a non-profit organization that offers families and individuals more constructive and peaceful ways of relating to each other and oneself in order to help end the cycle of family violence.  Her work utilized a range of psychological techniques and Spiritual encouragement. 
</p>
<p>For the public, Lindsay has been offering experiential "Quiet the Mind & Open the Heart" retreats and workshops. These programs are designed to help us access more deeply the peace and joy which is naturally within us and to realize how the conscious and unconscious concepts we carry in the mind often have a life diminishing influence on others and ourselves.  For many, it can be the catalyst needed to break through old or undesirable patterns affecting our family dynamics, intimate relationships, self-image, parenting, friendships and our work/career. 
</p>


