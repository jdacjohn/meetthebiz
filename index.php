<td valign="top"><span>Meet The Creator: David Zimmerman</span>
<p>	David Zimmerman has been coaching actors and putting together workshops for over a decade. As a personal coach, he has been hired by production companies such as Ryan Murphy's ground breaking series NIP/TUCK and the Weinstein Company's feature film, UNKNOWN. David is the Creator and Producer of the "MEET THE BIZ" workshops which looks to make diversity a common place.</p>

	<p>As a Casting Director his credits include the Showtime pilots and Star Price Productions "PROJECT X" and "SHADOW POINT", as well as the popular series "PENN &amp; TELLER'S BULLSHIT." He has also cast feature films including: "FIGHTING WORDS," which the Los Angeles reviews called his cast "solid and diverse." Working with Joey Travolta and Inclusion Films on the film "SPUD" was a project close to his heart. Zimmerman has also enjoyed working with Limetime TV, Mark Wolper Productions, and Time Life Productions. David just finished co-casting Director/Writer Ringo Le's and Producer Quentin Lee's "BIG GAY LOVE" with Stanzi Stokes and is the casting director for James Helsing's murder mystery thriller, MODUS OPERANDI, starring Dame Eileen Atkins (BEAUTIFUL CREATURES) and Bruce Davison (X-MEN).</p>

	<p>You can also see him on the screen as an actor in such films as "HIDE", "MEET THE FOCKERS", and the webseries, "CHILD OF THE '70s."</p>

	<p>On October 14, 2007, David was honored at the 25 Annual Media Access Awards at Universal Studios Globe Theatre with the prestigious MEDIA ACCESS AWARD.
